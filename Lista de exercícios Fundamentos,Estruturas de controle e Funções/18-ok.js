let converteExtenso = (numero) =>{
    switch(numero){
        case 0:
            return console.log('Número zero.')
            break
        case 1:
            return console.log('Número um.')
            break
        case 2:
            return console.log('Número dois.')
            break
        case 3:
            return console.log('Número três.')
            break
        case 4:
            return console.log('Número quatro.')
            break
        case 5:
            return console.log('Número cinco.')
            break
        case 6:
            return console.log('Número seis.')
            break
        case 7:
            return console.log('Número sete.')
            break
        case 8:
            return console.log('Número oito.')
            break
        case 9:
            return console.log('Número nove.')
            break
        case 10:
            return console.log('Número dez.')
            break
        default:
            return console.log('Número fora do invervalo.')
        }
}

converteExtenso(0)
converteExtenso(1)
converteExtenso(2)
converteExtenso(3)
converteExtenso(4)
converteExtenso(5)
converteExtenso(6)
converteExtenso(7)
converteExtenso(8)
converteExtenso(9)
converteExtenso(10)
converteExtenso(11)
converteExtenso(-1)